    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'email' => 'required',
            'username' => 'required',
            'password' => 'required',
        ]);
        User::create([
            'name' => $request->name,
            'email' => $request->email,
            'username' => $request->username,
            'password' => md5($request->password),
        ]);
        return view('login')->with('Berhasil','Anda telah berhasil melakukan registrasi');
    }

